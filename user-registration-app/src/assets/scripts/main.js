function clkBtnScdAct(){
    if(document.getElementById("buttAct").style.backgroundColor == "white"){
        document.getElementById("buttAct").style.backgroundColor = "#0071ba";
        document.getElementById("buttAct").style.color = "white";
    }else{
        document.getElementById("buttAct").style.backgroundColor = "white";
        document.getElementById("buttAct").style.color = "#0071ba";
    }
}

